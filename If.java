package test;

public class If {
	public static void main(String[] args) {
		int value = 0;
		
		if (value > 0) {
			System.out.println("value is positive");
		} else if (value < 0) {
			System.out.println("value is negative");
		} else {
			System.out.println("value is 0");
		}
	}
}
